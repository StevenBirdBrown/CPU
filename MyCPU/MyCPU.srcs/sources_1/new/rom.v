    input [15:0] addr,
    input ena, 
    input read,
    output [15:0] data
    );
    reg [15:0] rom [255:0];
    initial begin
        //Program 1, LED
        rom[42]=16'b01111_001_000_00001;
        rom[43]=16'b10000_001_000_00000;
        rom[44]=16'b01110_000_001_00000;
        rom[45]=16'b10100_100_000_00000;
        rom[46]=16'b01011_000_000_00000;
        rom[47]=16'b11001_100_000_00010;
        rom[48]=16'b11111_100_000_00000;
        //sum_i=1^100
        rom[74]=16'b01111_010_0110_0101;
        rom[75]=16'b10000_010_0000_0000;
        rom[76]=16'b01111_000_0000_0001;
        rom[77]=16'b10000_000_0000_0000;
        rom[78]=16'b01111_001_0000_0010;
        rom[79]=16'b10000_001_000_00000;
        rom[80]=16'b00000_000_001_00000;
        rom[81]=16'b00100_001_000_00001;
        rom[82]=16'b00101_010_001_00000;
        rom[83]=16'b11011_100_000_00011;
        rom[84]=16'b01111_100_1000_0011;
        rom[85]=16'b10000_100_0000_0001;
        rom[86]=16'b10100_100_000_00000;
        rom[87]=16'b11111_000_000_00000;
    end
    assign data=(read&&ena)?rom[addr]:16'hzzzz;
endmodule
